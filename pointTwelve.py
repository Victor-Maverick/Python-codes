number_Of_Years = 10
amount_Invested = 1000
annual_Rate = 7 / 100
tenth_Amount_On_Deposit = amount_Invested * (1 + annual_Rate)**number_Of_Years

print('the money after 10 years will be', tenth_Amount_On_Deposit)


twentieth_Year = 20

twentieth_Amount_On_Deposit = amount_Invested * (1 + annual_Rate)**twentieth_Year

print('the money after 20 years will be', twentieth_Amount_On_Deposit)


thirtieth_Year = 30

thirtieth_Amount_On_Deposit = amount_Invested * (1 + annual_Rate)**thirtieth_Year

print('the money after 30 years will be', thirtieth_Amount_On_Deposit)